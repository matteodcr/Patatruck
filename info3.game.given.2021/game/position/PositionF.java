package position;

public class PositionF {
	private float m_x;
	private float m_y;

	public PositionF(float x, float y) {
		m_x = x;
		m_y = y;
	}

	public float getX() {
		return m_x;
	}

	public void setX(float x) {
		m_x = x;
	}

	public float getY() {
		return m_y;
	}

	public void setY(float y) {
		m_y = y;
	}
}
